"use strict";
/**
 * 
 * @param {number} a 
 * @param {number} b 
 * @returns {number} 
 */
function compound(a, b) {
    return a + b;
}
/**
 * 
 * @param {number} a 
 * @param {number} b 
 * @returns {number} 
 */
function subtraction(a, b) {
    return a - b
}


/**
 * 
 * @param {number} a 
 * @param {number} b 
 * @returns {number} 
 */
function multiplication(a, b) {
    return a * b
}

/**
 * 
 * @param {number} a 
 * @param {number} b 
 * @returns {number} 
 */
function division(a, b) {
    return a / b
}